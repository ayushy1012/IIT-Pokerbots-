@dataclass
class OppStats:
    hands: int = 0

    preflop_seen: int = 0
    preflop_raises: int = 0
    preflop_3bets: int = 0
    preflop_jams: int = 0
    preflop_hands: int = 0
    preflop_raise_hands: int = 0

    postflop_seen: int = 0
    postflop_aggr: int = 0

    folds_to_bet: int = 0
    fold_opp: int = 0

    flop_raises_vs_bet: int = 0
    flop_raises_opp: int = 0
    turn_raises_vs_bet: int = 0
    turn_raises_opp: int = 0
    river_raises_vs_bet: int = 0
    river_raises_opp: int = 0

    # NEW: flop cbet bandit tracking
    flop_cbets_faced: int = 0
    flop_cbet_folds: int = 0

    opp_bid_sum: float = 0.0
    opp_bid_cnt: int = 0
    opp_bid_hist: List[int] = None

    river_bet_showdowns: int = 0
    river_bluffs: int = 0

    jam_showdowns: int = 0
    jam_strength_sum: float = 0.0

    def __post_init__(self):
        if self.opp_bid_hist is None:
            self.opp_bid_hist = []

    def opp_bid_q(self, q: float) -> float:
        if not self.opp_bid_hist:
            return 0.0
        s = sorted(self.opp_bid_hist)
        idx = int(max(0, min(len(s) - 1, round(q * (len(s) - 1)))))
        return float(s[idx])

    def opp_bid_modes(self, k: int = 2, window: int = 80) -> List[int]:
        if not self.opp_bid_hist:
            return []
        w = self.opp_bid_hist[-window:]
        c = Counter(w)
        return [v for v, _ in c.most_common(k)]

    def fold_to_bet_rate(self) -> float:
        return (self.folds_to_bet + 2.0) / (self.fold_opp + 4.0) if self.fold_opp else 0.45

    def postflop_aggr_rate(self) -> float:
        return self.postflop_aggr / self.postflop_seen if self.postflop_seen else 0.18

    def pf_raise_rate(self) -> float:
        if not self.preflop_hands:
            return 0.35
        # Track raise frequency by hand rather than by every preflop action,
        # which is more stable for range modeling and sizing decisions.
        return (self.preflop_raise_hands + 1.5) / (self.preflop_hands + 4.0)

    def jam_rate(self) -> float:
        return self.preflop_jams / self.hands if self.hands else 0.0

    def jam_strength_mean(self) -> float:
        return self.jam_strength_sum / self.jam_showdowns if self.jam_showdowns else 0.0

    def river_bluff_rate(self) -> float:
        return (self.river_bluffs + 1.0) / (self.river_bet_showdowns + 4.0) if self.river_bet_showdowns else 0.25

    def sample_river_bluff_freq(self, rng: random.Random) -> float:
        # Prior: bluff ~ 25% (Beta 1,3) – stable early, converges late.
        alpha = float(self.river_bluffs) + 1.0
        value = max(0, self.river_bet_showdowns - self.river_bluffs)
        beta = float(value) + 3.0
        return float(rng.betavariate(alpha, beta))

    def flop_raise_vs_bet_rate(self) -> float:
        return self.flop_raises_vs_bet / self.flop_raises_opp if self.flop_raises_opp else 0.0

    def river_raise_vs_bet_rate(self) -> float:
        return self.river_raises_vs_bet / self.river_raises_opp if self.river_raises_opp else 0.0

    def sample_flop_fold_freq(self, rng: random.Random) -> float:
        # Prior: fold flop ~ 40% (Beta 2,3). TS avoids overreacting early.
        alpha = float(self.flop_cbet_folds) + 2.0
        beta = float(max(0, self.flop_cbets_faced - self.flop_cbet_folds)) + 3.0
        return float(rng.betavariate(alpha, beta))

    def archetype(self) -> str:
        j = self.jam_rate()
        a = self.postflop_aggr_rate()
        r = self.pf_raise_rate()
        f = self.fold_to_bet_rate()
        fr = self.flop_raise_vs_bet_rate()
        rr = self.river_raise_vs_bet_rate()

        if j >= 0.22:
            return "JAMMER"
        if f <= 0.32 and a <= 0.22:
            return "STATION"
        if a >= 0.38 or fr >= 0.22:
            return "MANIAC"
        if r <= 0.22 and a <= 0.16:
            return "NIT"
        if rr >= 0.20 and self.river_bet_showdowns >= 6 and self.river_bluff_rate() < 0.30:
            return "VALUE_RAISER"
        return "REG"


