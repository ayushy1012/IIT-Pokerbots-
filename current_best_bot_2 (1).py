def _hand_class(hand: Sequence[str]) -> str:
    """169-class key like 'AKs', 'T9o', '77'."""
    a, b = hand
    ra, rb = a[0], b[0]
    sa, sb = a[1], b[1]
    ia, ib = RANKS.index(ra), RANKS.index(rb)
    if ia < ib:
        ra, rb = rb, ra
        sa, sb = sb, sa
    if ra == rb:
        return ra + ra
    return ra + rb + ("s" if sa == sb else "o")


def _board_texture(board: Sequence[str]) -> float:
    """0..1 wetness heuristic."""
    if len(board) < 3:
        return 0.0

    ranks = [RANK_TO_INT[c[0]] for c in board]
    suits = [c[1] for c in board]

    max_suit = max(suits.count(s) for s in set(suits))
    flush = 1.0 if max_suit >= 3 else (0.55 if max_suit == 2 else 0.0)

    uniq = sorted(set(ranks))
    span = uniq[-1] - uniq[0]
    straight = 1.0 if span <= 4 else (0.6 if span <= 7 else 0.25)

    paired = 0.35 if len(set(ranks)) < len(ranks) else 0.0
    return max(0.0, min(1.0, 0.50 * flush + 0.40 * straight + 0.10 * paired))


def _chen_strength(hand: Sequence[str]) -> float:
    """0..1 fast proxy (used for opponent-range sampling / jam strength summaries)."""
    c1, c2 = hand
    a = RANK_TO_INT[c1[0]]
    b = RANK_TO_INT[c2[0]]
    hi, lo = (a, b) if a >= b else (b, a)
    suited = (c1[1] == c2[1])
    pair = (hi == lo)
    gap = hi - lo - 1

    base_map = {
        14: 10.0, 13: 8.0, 12: 7.0, 11: 6.0, 10: 5.0,
        9: 4.5, 8: 4.0, 7: 3.5, 6: 3.0, 5: 2.5,
        4: 2.0, 3: 1.5, 2: 1.0
    }
    score = base_map[hi]

    if pair:
        score = max(5.0, score * 2.0)
        if hi >= 12:
            score += 2.0
    else:
        if suited:
            score += 2.0
        if gap == 0:
            pass
        elif gap == 1:
            score -= 1.0
        elif gap == 2:
            score -= 2.0
        elif gap == 3:
            score -= 4.0
        else:
            score -= 5.0
        if gap <= 1 and hi < 12:
            score += 1.0

    score = max(0.0, min(20.0, score))
    return score / 20.0


def _chen_to_eq(s: float) -> float:
    # conservative mapping (ONLY used to fill missing PF table entries)
    eq = 0.30 + 0.58 * s - 0.05 * s * s
    return max(0.22, min(0.88, eq))


def _class_proxy_strength(hc: str) -> float:
    if len(hc) == 2:  # pair
        r = hc[0]
        return _chen_strength((r + "s", r + "h"))
    r1, r2, suited = hc[0], hc[1], (hc[2] == "s")
    if suited:
        return _chen_strength((r1 + "s", r2 + "s"))
    return _chen_strength((r1 + "s", r2 + "h"))


def _make_all_169_keys() -> List[str]:
    out: List[str] = []
    for i in range(len(RANKS) - 1, -1, -1):
        for j in range(i, -1, -1):
            r1 = RANKS[i]
            r2 = RANKS[j]
            if r1 == r2:
                out.append(r1 + r2)
            else:
                out.append(r1 + r2 + "s")
                out.append(r1 + r2 + "o")
    return out


def _made_draw_features(hand: Sequence[str], board: Sequence[str]) -> Dict[str, float]:
    """Cheap made-hand + draw features for guardrails."""
    cards = list(hand) + list(board)
    ranks = [RANK_TO_INT[c[0]] for c in cards]
    suits = [c[1] for c in cards]

    rc = Counter(ranks)
    counts = sorted(rc.values(), reverse=True)

    made = "high"
    if counts and counts[0] == 4:
        made = "quads"
    elif counts and counts[0] == 3 and len(counts) > 1 and counts[1] >= 2:
        made = "full_house"
    elif max(suits.count(s) for s in set(suits)) >= 5:
        made = "flush"
    else:
        uniq = sorted(set(ranks))
        if 14 in uniq:
            uniq = [1] + uniq
        best_run = 1
        run = 1
        for i in range(1, len(uniq)):
            if uniq[i] == uniq[i - 1] + 1:
                run += 1
                best_run = max(best_run, run)
            else:
                run = 1
        if best_run >= 5:
            made = "straight"
        elif counts and counts[0] == 3:
            made = "trips"
        elif counts and counts[0] == 2 and len(counts) > 1 and counts[1] == 2:
            made = "two_pair"
        elif counts and counts[0] == 2:
            made = "pair"

    flush_draw = 1.0 if max(suits.count(s) for s in set(suits)) == 4 else 0.0

    uniq2 = sorted(set(ranks))
    if 14 in uniq2:
        uniq2 = [1] + uniq2
    straight_draw = 0.0
    for start in range(1, 11):
        need = 0
        for v in range(start, start + 5):
            if v not in uniq2:
                need += 1
        if need == 1:
            straight_draw = 1.0
            break
        if need == 0:
            straight_draw = 0.0
            break

    return {"made": made, "flush_draw": flush_draw, "straight_draw": straight_draw}


def _made_rank(made: str) -> int:
    order = {
        "high": 0, "pair": 1, "two_pair": 2, "trips": 3,
        "straight": 4, "flush": 5, "full_house": 6, "quads": 7
    }
    return order.get(made, 0)


def _hand_profile(hand: Sequence[str], board: Sequence[str]) -> Dict[str, object]:
    hr = [RANK_TO_INT[c[0]] for c in hand]
    br = [RANK_TO_INT[c[0]] for c in board]
    hs = [c[1] for c in hand]
    bs = [c[1] for c in board]

    topb = max(br) if br else 0
    br_sorted = sorted(br, reverse=True)
    secondb = br_sorted[1] if len(br_sorted) >= 2 else 0
    paired_board = (len(set(br)) < len(br)) if br else False

    all_r = hr + br
    rc = Counter(all_r)

    pocket_pair = (hr[0] == hr[1])
    overpair = pocket_pair and hr[0] > topb
    underpair = pocket_pair and hr[0] < topb
    set_trips = pocket_pair and rc[hr[0]] == 3

    top_pair = False
    second_pair = False
    pair_rank = 0
    kicker = 0

    if pocket_pair:
        pair_rank = hr[0]
        kicker = max(br) if br else 0
    else:
        for r in hr:
            if rc[r] >= 2:
                pair_rank = r
                other = hr[0] if hr[1] == r else hr[1]
                kicker = other
                break

    if pair_rank:
        if pair_rank == topb:
            top_pair = True
        elif pair_rank == secondb:
            second_pair = True

    suit_counts = Counter(bs + hs)
    max_suit = max(suit_counts.values()) if suit_counts else 0
    flush_draw = (max_suit == 4)
    backdoor_flush = (len(board) == 3 and max_suit == 3 and not flush_draw)
    overcards = sum(1 for r in hr if r > topb) if br else 0

    return {
        "topb": topb,
        "secondb": secondb,
        "paired_board": paired_board,
        "pocket_pair": pocket_pair,
        "set_trips": set_trips,
        "overpair": overpair,
        "underpair": underpair,
        "pair_rank": pair_rank,
        "kicker": kicker,
        "top_pair": top_pair,
        "second_pair": second_pair,
        "flush_draw": flush_draw,
        "backdoor_flush": backdoor_flush,
        "overcards": overcards,
    }


